package pe.com.sedapal.scr.correo.core.beans;

import java.util.List;

public class MensajeCorreo {
	
	private String strIdMensaje;
	private String strAsunto;
	private String strRemitente;
	private String strNombreAdjuntoConfig;
	private List<AdjuntoMensaje> lstAdjuntos;
	private String strContenido;
	private List<String> lstDestinatarios;
	private String strPara;
	private List<String> lstCcs;
	private String strConCopia;
	private String strNombreAdjuntoGuardado;
	
	private boolean bolValid;
	private String strMensajeError;
	
	public MensajeCorreo() {}

	public String getStrIdMensaje() {
		return strIdMensaje;
	}

	public void setStrIdMensaje(String strIdMensaje) {
		this.strIdMensaje = strIdMensaje;
	}

	public String getStrAsunto() {
		return strAsunto;
	}

	public void setStrAsunto(String strAsunto) {
		this.strAsunto = strAsunto;
	}

	public String getStrRemitente() {
		return strRemitente;
	}

	public void setStrRemitente(String strRemitente) {
		this.strRemitente = strRemitente;
	}

	public String getStrNombreAdjuntoConfig() {
		return strNombreAdjuntoConfig;
	}

	public void setStrNombreAdjuntoConfig(String strNombreAdjuntoConfig) {
		this.strNombreAdjuntoConfig = strNombreAdjuntoConfig;
	}

	public List<AdjuntoMensaje> getLstAdjuntos() {
		return lstAdjuntos;
	}

	public void setLstAdjuntos(List<AdjuntoMensaje> lstAdjuntos) {
		this.lstAdjuntos = lstAdjuntos;
	}

	public String getStrContenido() {
		return strContenido;
	}

	public void setStrContenido(String strContenido) {
		this.strContenido = strContenido;
	}

	public List<String> getLstDestinatarios() {
		return lstDestinatarios;
	}

	public void setLstDestinatarios(List<String> lstDestinatarios) {
		this.lstDestinatarios = lstDestinatarios;
	}

	public String getStrPara() {
		return strPara;
	}

	public void setStrPara(String strPara) {
		this.strPara = strPara;
	}

	public List<String> getLstCcs() {
		return lstCcs;
	}

	public void setLstCcs(List<String> lstCcs) {
		this.lstCcs = lstCcs;
	}

	public String getStrConCopia() {
		return strConCopia;
	}

	public void setStrConCopia(String strConCopia) {
		this.strConCopia = strConCopia;
	}

	public String getStrNombreAdjuntoGuardado() {
		return strNombreAdjuntoGuardado;
	}

	public void setStrNombreAdjuntoGuardado(String strNombreAdjuntoGuardado) {
		this.strNombreAdjuntoGuardado = strNombreAdjuntoGuardado;
	}		
	
	public boolean isBolValid() {
		return bolValid;
	}		
	
	public void setBolValid(boolean bolValid) {
		this.bolValid = bolValid;
	}		
	
	public String getStrMensajeError() {
		return strMensajeError;
	}		
	
	public void setStrMensajeError(String strMensajeError) {
		this.strMensajeError = strMensajeError;
	}		
}
