package pe.com.sedapal.common.core.beans;

public class Paginacion {
	
	private Integer colorderby;
	private String colorderbydir;
	private Integer pagdesde;
	private Integer cantidadpag;
	public Integer getColorderby() {
		return colorderby;
	}
	public void setColorderby(Integer colorderby) {
		this.colorderby = colorderby;
	}
	public String getColorderbydir() {
		return colorderbydir;
	}
	public void setColorderbydir(String colorderbydir) {
		this.colorderbydir = colorderbydir;
	}
	public Integer getPagdesde() {
		return pagdesde;
	}
	public void setPagdesde(Integer pagdesde) {
		this.pagdesde = pagdesde;
	}
	public Integer getCantidadpag() {
		return cantidadpag;
	}
	public void setCantidadpag(Integer cantidadpag) {
		this.cantidadpag = cantidadpag;
	}
	@Override
	public String toString() {
		return "Paginacion [colorderby=" + colorderby + ", colorderbydir=" + colorderbydir + ", pagdesde=" + pagdesde
				+ ", cantidadpag=" + cantidadpag + "]";
	}
	
	
	
	
	
	

}
