package pe.com.sedapal.scr.core.beans.common;


/**
 * The Enum Estado.
 */
public enum Estado {

	/** The inactivo. */
	INACTIVO("Inactivo", 0), /** The activo. */
 ACTIVO("Activo", 1), /** The eliminado. */
 ELIMINADO("Eliminado", 2), /** The todos. */
 TODOS("Todos",
			-1), 
 /** The inactivo activo. */
 INACTIVO_ACTIVO("Inactivo/Activo", -2);

	/** The descripcion. */
	private String descripcion;
	
	/** The valor. */
	private int valor;

	/**
	 * Instantiates a new estado.
	 *
	 * @param descripcion the descripcion
	 * @param valor the valor
	 */
	private Estado(String descripcion, int valor) {
		this.descripcion = descripcion;
		this.valor = valor;
	}

	/**
	 * Gets the descripcion.
	 *
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * Sets the descripcion.
	 *
	 * @param descripcion the new descripcion
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	/**
	 * Gets the valor.
	 *
	 * @return the valor
	 */
	public int getValor() {
		return valor;
	}

	/**
	 * Sets the valor.
	 *
	 * @param valor the new valor
	 */
	public void setValor(int valor) {
		this.valor = valor;
	}

}
