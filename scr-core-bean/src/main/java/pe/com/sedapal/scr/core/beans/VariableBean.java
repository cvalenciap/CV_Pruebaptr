package pe.com.sedapal.scr.core.beans;

import java.io.Serializable;

public class VariableBean extends BaseSptarBean implements Serializable {
	
	public static final long serialVersionUID = 1L;
	
	public Integer idVariable;
	public String descripcionVariable;
	public String descripcionLetra;
	public String estado;
	public String descripcionConcat;
	public Integer idParametro;
	public String descripcionParametro;
	
	
	public String getDescripcionConcat() {
		return descripcionConcat;
	}
	public void setDescripcionConcat(String descripcionConcat) {
		this.descripcionConcat = descripcionConcat;
	}
	/**
	 * @return the descripcionLetra
	 */
	public String getDescripcionLetra() {
		return descripcionLetra;
	}
	/**
	 * @param descripcionLetra the descripcionLetra to set
	 */
	public void setDescripcionLetra(String descripcionLetra) {
		this.descripcionLetra = descripcionLetra;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public Integer getIdVariable() {
		return idVariable;
	}
	public void setIdVariable(Integer idVariable) {
		this.idVariable = idVariable;
	}
	public String getDescripcionVariable() {
		return descripcionVariable;
	}
	public void setDescripcionVariable(String descripcionVariable) {
		this.descripcionVariable = descripcionVariable;
	}
	public Integer getIdParametro() {
		return idParametro;
	}
	public void setIdParametro(Integer idParametro) {
		this.idParametro = idParametro;
	}
	public String getDescripcionParametro() {
		return descripcionParametro;
	}
	public void setDescripcionParametro(String descripcionParametro) {
		this.descripcionParametro = descripcionParametro;
	}
}
